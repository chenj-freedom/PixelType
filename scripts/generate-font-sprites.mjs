const CELL = 1;
const GLYPH_COLUMNS = 12;
const GLYPH_ROWS = 14;
const PADDING_X = 0;
const PADDING_Y = 1;
const INK = [6, 22, 46, 255];

mkdirSync(outDir, { recursive: true });

for (const char of Object.keys(GLYPHS)) {
  const fileName = /[A-Z]/.test(char) ? `upper-${char}.png` : `lower-${char}.png`;
  writeFileSync(join(outDir, fileName), renderGlyphPng(GLYPHS[char], char));
}

function renderGlyphPng(rows, char) {
  const normalizedRows = normalizeRowsForRender(rows, char);
  const width = GLYPH_COLUMNS * CELL + PADDING_X * 2;
  const height = GLYPH_ROWS * CELL + PADDING_Y * 2;
  const pixels = new Uint8Array(width * height * 4);

  normalizedRows.slice(0, GLYPH_ROWS).forEach((row, y) => {
    row.padEnd(GLYPH_COLUMNS, '0').slice(0, GLYPH_COLUMNS).split('').forEach((bit, x) => {
      if (bit !== '1') return;
      paintCell(pixels, width, x * CELL + PADDING_X, y * CELL + PADDING_Y, CELL, CELL);
    });
  });

  return encodePng(width, height, pixels);
}

function normalizeRowsForRender(rows, char) {
  if (!/[A-Z]/.test(char)) return rows;
  const normalizedRows = [...rows];
  if (!normalizedRows[10] || /^0+$/.test(normalizedRows[10])) {
    normalizedRows[10] = normalizedRows[9] || '000000000000';
  }
  return normalizedRows;
}

function paintCell(pixels, width, x, y, w, h) {
  for (let py = y; py < y + h; py += 1) {
    for (let px = x; px < x + w; px += 1) {
      const offset = (py * width + px) * 4;
      pixels[offset] = INK[0];
      pixels[offset + 1] = INK[1];
      pixels[offset + 2] = INK[2];
      pixels[offset + 3] = INK[3];
    }
  }
}

function encodePng(width, height, rgba) {
  const stride = width * 4;
  const raw = Buffer.alloc((stride + 1) * height);
  for (let y = 0; y < height; y += 1) {
    raw[y * (stride + 1)] = 0;
    Buffer.from(rgba.buffer, y * stride, stride).copy(raw, y * (stride + 1) + 1);
  }

  return Buffer.concat([
    Buffer.from([137, 80, 78, 71, 13, 10, 26, 10]),
    chunk('IHDR', ihdr(width, height)),
    chunk('IDAT', deflateSync(raw)),
    chunk('IEND', Buffer.alloc(0)),
  ]);
}

function ihdr(width, height) {
  const data = Buffer.alloc(13);
  data.writeUInt32BE(width, 0);
  data.writeUInt32BE(height, 4);
  data[8] = 8;
  data[9] = 6;
  return data;
}

function chunk(type, data) {
  const typeBuffer = Buffer.from(type);
  const length = Buffer.alloc(4);
  length.writeUInt32BE(data.length, 0);
  const crc = Buffer.alloc(4);
  crc.writeUInt32BE(crc32(Buffer.concat([typeBuffer, data])), 0);
  return Buffer.concat([length, typeBuffer, data, crc]);
}

function crc32(buffer) {
  let crc = 0xffffffff;
  for (const byte of buffer) {
    crc ^= byte;
    for (let i = 0; i < 8; i += 1) {
      crc = (crc >>> 1) ^ (0xedb88320 & -(crc & 1));
    }
  }
  return (crc ^ 0xffffffff) >>> 0;
}
